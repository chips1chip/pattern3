public class Pattern3 {
    public static void main(String[] args) {
        // Printing the first row of the pattern with stars at the ends
        System.out.println("*   *");

        // Printing the second row with stars at the second and fourth positions
        System.out.println(" * * ");

        // Printing the third row with a single star in the middle
        System.out.println("  *  ");

        // Printing the fourth row with stars at the second and fourth positions
        System.out.println(" * * ");

        // Printing the fifth and final row, identical to the first row
        System.out.println("*   *");
    }
}
